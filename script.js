const menuToggle = document.querySelector('.menu-toggle');
const nav = document.querySelector('.global-nav');
if (menuToggle && nav) {
  menuToggle.addEventListener('click', () => nav.classList.toggle('open'));
  nav.querySelectorAll('a:not(.nav-parent)').forEach(link => link.addEventListener('click', () => nav.classList.remove('open')));
}
document.querySelectorAll('[data-dropdown]').forEach(dropdown => {
  const button = dropdown.querySelector('.nav-parent');
  if (!button) return;

  button.addEventListener('click', event => {
    if (window.matchMedia('(max-width: 780px)').matches) {
      event.preventDefault();
    }
    event.stopPropagation();
    const isOpen = dropdown.classList.toggle('open');
    button.setAttribute('aria-expanded', String(isOpen));
  });

  dropdown.querySelectorAll('a:not(.nav-parent)').forEach(link => {
    link.addEventListener('click', () => {
      dropdown.classList.remove('open');
      button.setAttribute('aria-expanded', 'false');
    });
  });
});
document.addEventListener('click', () => {
  document.querySelectorAll('[data-dropdown].open').forEach(dropdown => {
    dropdown.classList.remove('open');
    const button = dropdown.querySelector('.nav-parent');
    if (button) button.setAttribute('aria-expanded', 'false');
  });
});
document.querySelectorAll('[data-tabs]').forEach(tabGroup => {
  const buttons = tabGroup.querySelectorAll('.tab-btn');
  const panels = tabGroup.querySelectorAll('.tab-panel');
  buttons.forEach(button => {
    button.addEventListener('click', () => {
      const target = button.dataset.tab;
      buttons.forEach(btn => btn.classList.remove('active'));
      panels.forEach(panel => panel.classList.remove('active'));
      button.classList.add('active');
      const activePanel = tabGroup.querySelector(`#${target}`);
      if (activePanel) activePanel.classList.add('active');
    });
  });
});
document.querySelectorAll('[data-accordion] .accordion-header').forEach(header => {
  header.addEventListener('click', () => header.parentElement.classList.toggle('open'));
});

