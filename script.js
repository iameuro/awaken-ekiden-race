const normalizeI18nText = text => text.replace(/\s+/g, ' ').trim();
const textNodes = [];
const walker = document.createTreeWalker(document.body, NodeFilter.SHOW_TEXT, {
  acceptNode(node) {
    const value = normalizeI18nText(node.nodeValue);
    if (!value) return NodeFilter.FILTER_REJECT;
    const parent = node.parentElement;
    if (!parent || parent.closest('script, style')) return NodeFilter.FILTER_REJECT;
    return NodeFilter.FILTER_ACCEPT;
  }
});

while (walker.nextNode()) {
  const node = walker.currentNode;
  textNodes.push({ node, ko: normalizeI18nText(node.nodeValue) });
}

const originalTitle = document.title;
const htmlRoot = document.documentElement;
const menuButton = document.querySelector('.menu-toggle');
function getSavedLanguage() {
  try {
    return localStorage.getItem('site-language');
  } catch (error) {
    return null;
  }
}

function saveLanguage(lang) {
  try {
    localStorage.setItem('site-language', lang);
  } catch (error) {
    // Language switching still works for the current page if storage is unavailable.
  }
}

function setLanguage(lang) {
  const dictionary = translations[lang] || {};
  textNodes.forEach(({ node, ko }) => {
    node.nodeValue = lang === 'ko' ? ko : dictionary[ko] || ko;
  });

  document.title = lang === 'ko' ? originalTitle : titleTranslations[lang]?.[originalTitle] || originalTitle;
  htmlRoot.lang = lang;
  if (menuButton) {
    menuButton.setAttribute('aria-label', lang === 'en' ? 'Open menu' : lang === 'ja' ? 'メニューを開く' : '메뉴 열기');
  }

  document.querySelectorAll('.language-switcher button').forEach(button => {
    const isActive = button.dataset.lang === lang;
    button.classList.toggle('active', isActive);
    button.setAttribute('aria-pressed', String(isActive));
  });

  saveLanguage(lang);
}

document.querySelectorAll('.language-switcher button').forEach(button => {
  button.addEventListener('click', event => {
    event.stopPropagation();
    setLanguage(button.dataset.lang || 'ko');
  });
});

setLanguage(getSavedLanguage() || 'ko');
